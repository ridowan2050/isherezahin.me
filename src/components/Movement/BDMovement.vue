                  <h3  id="চার দফা">চার দফা</h3>
                  <ul>
                    <li>ইন্টারনেট সংযোগ চালু করা,</li>
                    <li>শিক্ষার্থীদের হলে আসার ব্যবস্থা করে দিয়ে হল খুলে দেওয়া,</li>
                    <li>আন্দোলনের সমন্বয়কদের নিরাপত্তা নিশ্চিত করা এবং </li>
                    <li>কারফিউ তুলে দেওয়া।</li>
                  </ul>
                  <p>এ বিষয়ে সংবাদ সম্মেলন করেছেন চারজন সমন্বয়ক।</p>
                  <p>একই দিনে বিরাজমান পরিস্থিতি নিয়ে বৈষম্যবিরোধী ছাত্র আন্দোলনের ৫৬ সমন্বয়কের যৌথ বিবৃতি’ শিরোনামে একটি খুদে বার্তা গণমাধ্যমকর্মীদের মুঠোফোনে পাঠানো হয়। যৌথ বিবৃতিতে ‘কমপ্লিট শাটডাউন’ (সর্বাত্মক অবরোধ) কর্মসূচি আরও জোরদার করার আহ্বান জানানো হয়। এতে বলা হয়, শুধু আদালতের রায়ের মাধ্যমে হত্যার দায় এড়াতে পারে না সরকার। বিবৃতিতে অভিযোগ করা হয়, কোটা সংস্কার আন্দোলনকে কেন্দ্র করে সরকার সাধারণ শিক্ষার্থীদের ওপর নির্যাতন চালিয়ে যাচ্ছে। ‘তিন শতাধিক’ ছাত্র-জনতাকে হত্যা করার অভিযোগ করা হয়েছে বিবৃতিতে।</p>
                  <p>এ ছাড়া বিবৃতিতে বলা হয়, বৈষম্যবিরোধী ছাত্র আন্দোলনের প্রথম সারির কয়েকজন সমন্বয়ককে পুলিশি হেফাজতে নিয়ে মনগড়া বক্তব্য আদায়ের চেষ্টা করে ব্যর্থ হয়েছে। সমন্বয়ক নাহিদ ইসলামের ওপর ব্যাপক নির্যাতন চালানো হয়েছে। এ ছাড়া সমন্বয়ক আসিফ মাহমুদ, আবু বাকের মজুমদারসহ কয়েকজনের সন্ধান দাবি করা হয়েছে।</p>

                  <p><span class="date-highlight">২২ জুলাই, সোমবার</span> কোটাপ্রথা সংস্কার করে আদালতের নির্দেশ অনুযায়ী তৈরি করা প্রজ্ঞাপন অনুমোদন দিয়েছেন প্রধানমন্ত্রী।</p>
                  <p>সংঘর্ষে আরও ১৩ জন নিহতের খোঁজ পাওয়া গেছে। তাঁদের মধ্যে পাঁচজন সোমবার ঢাকা মেডিকেল কলেজ হাসপাতালে চিকিৎসাধীন অবস্থায় মারা যান। নারায়ণগঞ্জে তিনজন গত শনিবার মারা যান, যাঁদের লাশ উদ্ধার করা হয় সোমবার। আর রাজধানীর স্যার সলিমুল্লাহ মেডিকেল কলেজ ও হাসপাতালে (মিটফোর্ড হাসপাতাল) গত শুক্রবার চারটি মরদেহ নেওয়া হয়েছিল বলে সোমবার খবর পাওয়া যায়। এর বাইরে একজন পুলিশ সদস্যের মৃত্যুর তথ্য এদিন জানা যায়। সব মিলিয়ে ছয় দিনে মোট ১৮৭ জনের মৃত্যুর খবর পাওয়া গেল। </p>

                  <p><span class="date-highlight">২৩ জুলাই, মঙ্গলবার</span> কোটাপ্রথা সংস্কার করে প্রজ্ঞাপন জারি।</p>
                  <p>মঙ্গলবার পর্যন্ত ঢাকাসহ বিভিন্ন জেলায় সংঘর্ষে ১৯৭ জনের মৃত্যুর খবর পাওয়া গেছে।</p>
                  <p>সর্বশেষ মঙ্গলবার ঢাকা মেডিকেল কলেজ হাসপাতালে চিকিৎসাধীন অবস্থায় মারা যান চট্টগ্রাম বিশ্ববিদ্যালয়ের (চবি) ছাত্র হৃদয় চন্দ্র তরুয়া (২২)। তিনি গত বৃহস্পতিবার চট্টগ্রামে গুলিবিদ্ধ হন।</p>
                  <p>মঙ্গলবার নতুন করে খোঁজ পাওয়া যায় আরও আটটি মৃত্যুর। এর মধ্যে ঢাকার ইনস্টিটিউট অব নিউরোসায়েন্সেস অ্যান্ড হাসপাতালে পাঁচজন এবং শহীদ সোহরাওয়ার্দী মেডিকেল কলেজ হাসপাতালে দুজনের মৃত্যু হয়। সাভারে এনাম মেডিকেল কলেজ হাসপাতালে খোঁজ পাওয়া গেছে নিহত আরেক ব্যক্তির।</p>
                  <p>রাতে সীমিত আকারে ব্রডব্যান্ড ইন্টারনেট সেবা চালু করা হয়।</p>

                  <p><span class="date-highlight">২৪ জুলাই, বুধবার</span> ঢাকাসহ বিভিন্ন জেলায় সংঘর্ষে আহত হয়ে চিকিৎসাধীন আরও চারজনের মৃত্যু হয়েছে। ঢাকা মেডিকেল কলেজ হাসপাতালে তিনজন ও গত মঙ্গলবার সাভারের এনাম মেডিকেল কলেজ হাসপাতালে একজনের মৃত্যু হয়।</p>
                  <p>সরকারি চাকরিতে কোটা সংস্কার আন্দোলনকে কেন্দ্র করে বিক্ষোভ ও পরবর্তী সংঘাতে এ নিয়ে ২০১ জনের মৃত্যুর খবর পাওয়া গেল।</p>
                  <p>কোটা আন্দোলনের প্ল্যাটফর্ম বৈষম্যবিরোধী ছাত্র আন্দোলনের অন্যতম সমন্বয়ক আসিফ মাহমুদ, আবু বাকের মজুমদার ও রিফাত রশীদের খোঁজ পাওয়া গেছে। নিখোঁজ থাকার পাঁচ দিন পর আসিফ ও বাকেরকে চোখ বাঁধা অবস্থায় ফেলে যাওয়া হয়েছে বলে ফেসবুকে পোস্ট দিয়ে দুজনই জানিয়েছেন। আর রিফাত <span class="clause-highlight">আত্মগোপনে</span> আছেন বলে জানা যায়।</p>

                  <p><span class="date-highlight">২৫ জুলাই, বৃহস্পতিবার</span> চিকিৎসাধীন আরও তিনজনের মৃত্যু। সব মিলিয়ে এখন পর্যন্ত মৃত্যু বেড়ে ২০৪ জন। ছুটির দুই দিন, শুক্র ও শনিবার কারফিউ শিথিল থাকবে ৯ ঘন্টা। অ্যামনেষ্টি প্রতিবেদন দিয়ে বলেছে, প্রাণঘাতী অস্ত্র ব্যবহার করেছে পুলিশ।</p>
                  <p>প্রধানমন্ত্রীর মেট্রোস্টেশন পরিদর্শন করে মেট্রো স্টেশনের অবস্থা দেখে কান্না করেন।</p>

                  <p><span class="date-highlight">২৬ জুলাই, শুক্রবার</span> এলাকা ভাগ করে চলছে <span class="clause-highlight">‘ব্লক রেইড’</span>। সারা দেশে অভিযান। সারা দেশে অন্তত ৫৫৫টি মামলা। গ্রেপ্তারের  সংখ্যা ৬ হাজার ২৬৪। চট্টগ্রামে ৩০ শিক্ষার্থী গ্রেপ্তারের খবর পাওয়া গেছে।</p>                  
                  <p>ঢাকাসহ বিভিন্ন জেলায় সংঘর্ষে আহত হয়ে চিকিৎসাধীন আরও তিনজনের মৃত্যু হয়েছে। সরকারি চাকরিতে কোটা সংস্কার আন্দোলনকে কেন্দ্র করে বিক্ষোভ ও পরবর্তী সংঘাতে এ নিয়ে ২০৯ জনের মৃত্যুর খবর পাওয়া গেল।</p>
                  <p>নাহিদ ইসলামসহ কোটা সংস্কার আন্দোলনের শিক্ষার্থীদের প্ল্যাটফর্ম বৈষম্যবিরোধী ছাত্র আন্দোলনের তিন সমন্বয়ককে রাজধানীর গণস্বাস্থ্য নগর হাসপাতাল থেকে নিজেদের হেফাজতে নিয়েছে ঢাকা মহানগর গোয়েন্দা পুলিশ (ডিবি)। অপর দুই সমন্বয়ক হলেন আসিফ মাহমুদ ও আবু বাকের মজুমদার। এঁরা তিনজনই ঢাকা বিশ্ববিদ্যালয়ের শিক্ষার্থী। একদল ব্যক্তি সাদাপোশাকে বেলা সাড়ে তিনটার দিকে ধানমন্ডির ওই হাসপাতাল থেকে তাঁদের তুলে নিয়ে যান। সে সময় ওই ব্যক্তিরা নিজেদের আইনশৃঙ্খলা রক্ষাকারী বাহিনীর সদস্য পরিচয় দেন। সেখানে উপস্থিত এক সমন্বয়কের স্বজন ও হাসপাতালের চিকিৎসকেরা এ তথ্য জানান।</p>
                  <p>এই তিনজনকে এর আগেও একবার তুলে নেওয়া হয়েছিল। কোটা সংস্কার আন্দোলনকারীরা দাবি আদায়ে সারা দেশে ‘কমপ্লিট শাটডাউন’ বা সর্বাত্মক অবরোধ কর্মসূচি দেওয়ার পর ১৯ জুলাই মধ্যরাতে খিলগাঁওয়ের নন্দীপাড়া থেকে নাহিদকে তুলে নেওয়া হয়। ২১ জুলাই ভোরে পূর্বাচল এলাকায় তাঁকে ফেলে যাওয়া হয়। নাহিদের শরীরের বিভিন্ন স্থানে ছিল আঘাতের চিহ্ন। এর পর থেকে তিনি গণস্বাস্থ্য নগর হাসপাতালে চিকিৎসাধীন ছিলেন। অপর দুই সমন্বয়ক আসিফ ও বাকেরকেও ১৯ জুলাই তুলে নেওয়া হয়েছিল। পাঁচ দিন পর ২৪ জুলাই আসিফকে হাতিরঝিল ও বাকেরকে ধানমন্ডি এলাকায় ফেলে যাওয়া হয়। এর পর থেকে আসিফও গণস্বাস্থ্য নগর হাসপাতালে চিকিৎসাধীন ছিলেন। হাসপাতালে তাঁর সঙ্গে থাকছিলেন বাকের।</p>

                  <p><span class="date-highlight">২৭ জুলাই, শনিবার</span> কোটা সংস্কার আন্দোলনের প্ল্যাটফর্ম বৈষম্যবিরোধী ছাত্র আন্দোলনের আরও দুই সমন্বয়ককে হেফাজতে নিয়েছে ঢাকা মহানগর পুলিশের গোয়েন্দা শাখা (ডিবি)। তাঁরা হলেন সারজিস আলম ও হাসনাত আবদুল্লাহ। এই দুজনকেও ব্যক্তিগত নিরাপত্তা দিতে ও সাম্প্রতিক ঘটনাবলি সম্পর্কে তথ্য জানতে হেফাজতে নেওয়া হয়েছে বলে ডিবি জানায়।</p>
                  <p>এখন পর্যন্ত ১১ দিনে গ্রেপ্তার ৯ হাজার ১২১ জন। এর মধ্যে রাজধানীতে গ্রেপ্তার ২ হাজার ৫৩৬ জন। অন্যদিকে হাসপাতালে আরেক মৃত্যু। সব মিলিয়ে সংঘর্ষে নিহত বেড়ে ২১০ জন। ঢাকায় কারফিউ শিথিল ১১ ঘণ্টা।</p>
                  <p>প্রধানমন্ত্রী শেখ হাসিনা জাতীয় অর্থোপেডিক হাসপাতাল ও পুনর্বাসন প্রতিষ্ঠানে (পঙ্গু হাসপাতাল) চিকিৎসাধীন আহত ব্যক্তিদের দেখতে গিয়েছেন। হাসপাতালে আহতদের দেখার পর সেতু ভবন, দুর্যোগ ব্যবস্থাপনা বিভাগ ও টোল প্লাজা দেখলেন প্রধানমন্ত্রী</p>

                  <p><span class="date-highlight">২৮ জুলাই, রোববার</span>মোবাইল ইন্টারনেট ১০ দিন পর সচল করা হয়।</p>
                  <p>এই দিন কোটা সংস্কার আন্দোলনের অন্যতম সমন্বয়ক নুসরাত তাবাসসুমকে হেফাজতে নিয়েছে ঢাকা মহানগর পুলিশের (ডিএমপি) গোয়েন্দা বিভাগ (ডিবি)। ভোরে রাজধানীর মিরপুরের রূপনগর আবাসিক এলাকার এক আত্মীয়ের বাসা থেকে তাকে উঠিয়ে নেওয়া হয়।</p>
                  <p>ডিবির হেফাজতে থাকা বৈষম্যবিরোধী ছাত্র আন্দোলনের ছয়জন সমন্বয়ক এক ভিডিও বার্তায় সব কর্মসূচি প্রত্যাহারের কথা বলেছেন। ডিবি কার্যালয়ে ধারণ করা একটি ভিডিও বার্তা রাত নয়টার দিকে গণমাধ্যমে পাঠানো হয়। ভিডিও বার্তায় কোটা সংস্কার আন্দোলনের প্ল্যাটফর্ম বৈষম্যবিরোধী ছাত্র আন্দোলনের সমন্বয়ক নাহিদ ইসলামকে একটি লিখিত বিবৃতি পাঠ করতে দেখা যায়। বিবৃতিতে যা লেখা ছিল তা হুবহু নিচে তুলে ধরা হলো:</p>
                  <blockquote>
                    <p>কোটা সংস্কার আন্দোলন ও তার প্রেক্ষিতে উদ্ভুত পরিস্থিতিতে অনেকেই অপ্রত্যাশিতভাবে আহত এবং নিহত হয়েছেন। তাছাড়া রাষ্ট্রীয় স্থাপনায় অগ্নিসংযোগসহ নানা সহিংস ঘটনা ঘটেছে। আমরা এ সকল অনাকাঙ্খিত ঘটনার তীব্র নিন্দা জানাই এবং সুষ্ঠু তদন্ত সাপেক্ষে দ্রুত বিচারের দাবি জানাচ্ছি।</p>
                    <p>আমাদের প্রধান দাবি ছিল কোটার যৌক্তিক সংস্কার, যা ইতিমধ্যে সরকার পূরণ করেছেন। এখন শিক্ষা প্রতিষ্ঠান খুলে দেওয়ার জন্য সরকারের প্রতি জোর আহবান জানাই। সার্বিক স্বার্থে আমরা এই মুহুর্ত থেকে আমাদের কর্মসূচি প্রত্যাহার করছি।</p>
                  </blockquote>
                  <p>উল্লেখ্য বিবৃতি দেওয়া ভিডিওতে আটকৃত সমন্বয়করা মোস কোড ব্যবহার করেছিল, যার ফলে সবাই বুঝে গিয়েছিল তাদেরকে জিম্মি করে এই বিবৃতি পাঠ করানো হয়েছিল। পরে পুলিশি হেফাজতে থাকা অবস্থায় পুলিশের অফিসে বসেই বাকি সমন্বয়কারীদের সাথে যোগাযোগ না করে এমন ঘোষণা দেয়ায় এই ঘোষণাকে সরকার ও পুলিশের চাপে দেয়া হয়েছে বলে আখ্যায়িত করে আন্দোলন চালিয়ে যাওয়ার ঘোষণা দেন বাকিরা।  তাদের ফেইসবুক বিবৃতিতে যা লেখা ছিল তা হুবহু নিচে তুলে ধরা হলো:</p>
                  <blockquote>
                    <p>“সিনিয়ররা আগেই নির্দেশনা দিয়েছিলেন এবং বৈষম্যবিরোধী ছাত্র আন্দোলনের শুরু থেকেই নীতিগত সিদ্ধান্ত ছিলো, সমন্বয়কদের মাঝে এক গ্রুপকে তুলে নিয়ে গেলে বাকিরা নেতৃত্ব দেবে। কাউকে যদি আটক করে জোরপূর্বক কোনো বিবৃতি আদায় করা হয়, তবে আমরা সেটা না মেনে যারা মাঠে থাকবো তারা আন্দোলন চালিয়ে যাবো। গণস্বাস্থ্য থেকে তুলে নিয়ে যাওয়ার আগে নাহিদ ভাই, আসিফ ভাই এবং বাকের ভাই তিনজনই স্পষ্ট করে বলেছিলেন, 'আমরা যদি না-ও থাকি আন্দোলন চালিয়ে নিবা, আন্দোলন যাতে থেমে না থাকে। আমাদের উপর অনেক ঝড়-ঝাপটা যাবে, যারা বাইরে থাকবে তারাই অন-কমান্ডে থাকবে। তাই সিনিয়রদের শেষ কথাগুলোকে বুকে ধারণ করেই আমরা আন্দোলন এগিয়ে নিয়ে যাচ্ছি। জোরপূর্বক আদায় করা বিবৃতি বাংলাদেশের ছাত্রসমাজ প্রত্যাখ্যান করছে।”</p>
                  </blockquote>
                  <p>মধ্যরাতে ‘সমন্বয়কদের কাছ থেকে জোর করে আন্দোলন প্রত্যাহারের বিবৃতি আদায়, সারা দেশে বিনা বিচারে হত্যা, গুম-খুন, মিথ্যা মামলা ও গণগ্রেপ্তারের প্রতিবাদে’ সারা দেশে বিক্ষোভ কর্মসূচি ঘোষণা করেন মুক্ত থাকা কয়েকজন সমন্বয়ক। ঢাকার আটটি স্থানে <span class="clause-highlight">বিক্ষোভ কর্মসূচি</span> হবে বলে সামাজিক যোগাযোগমাধ্যমে জানান সমন্বয়কেরা। এই স্থানগুলো হলো সায়েন্স ল্যাব, নর্থ সাউথ বিশ্ববিদ্যালয়ের ৮ নম্বর গেট, জাতীয় প্রেসক্লাব, উত্তরার বিএনএস সেন্টার, মিরপুর-১০, মিরপুরের ইসিবি চত্বর, রামপুরা ও মহাখালী।</p>

                  <p><span class="date-highlight">২৯ জুলাই, সোমবার</span> জামায়াত-শিবির নিষিদ্ধের সিদ্ধান্ত ১৪ দলের বৈঠকে নেওয়া হয়।</p>
                  <p>৬ সমন্বয়ক তখনও ডিবি হেফাজতে ছিল।</p>
                  <p>‘জাতিকে নিয়ে মশকরা কইরেন না’—এক শুনানিতে এ মন্তব্য করেছেন হাইকোর্ট। বৈষম্যবিরোধী ছাত্র আন্দোলনের ‘কথিত আটক’ ছয়জন সমন্বয়ককে অবিলম্বে মুক্তি দিতে ও দেশের বিভিন্ন স্থানে আন্দোলনকারীদের ওপর গুলি না চালাতে নির্দেশনা চেয়ে করা রিটের শুনানিতে রাষ্ট্রপক্ষের উদ্দেশে হাইকোর্ট এ মন্তব্য করেন। শুনানির একপর্যায়ে অতিরিক্ত অ্যাটর্নি জেনারেল মোহাম্মদ মেহেদী হাছান চৌধুরী বলেন, ‘গতকাল টিভিতে দেখেছি, এই ছয়জন (সমন্বয়ক) কাঁটাচামচ দিয়ে খাচ্ছে।’ একপর্যায়ে আদালত বলেন,</p>
                  <blockquote>
                    <p>“এগুলো করতে আপনাকে কে বলেছে? কেন করলেন এগুলো? জাতিকে নিয়ে মশকরা কইরেন না। যাকে নেন ধরে, একটি খাবার টেবিলে বসিয়ে দেন।”</p>
                  </blockquote>
                  <p>রাজধানীসহ দেশের বেশ কিছু স্থানে শিক্ষার্থীদের বিক্ষোভ কর্মসূচিতে বাধা ও হামলার ঘটনা ঘটেছে। সোমবার ঢাকাসহ বিভিন্ন স্থানে শিক্ষার্থীদের মিছিলে ছাত্রলীগ হামলা চালিয়েছে এবং পুলিশ লাঠিপেটা করেছে বলে অভিযোগ পাওয়া গেছে। এ সময় কিছু শিক্ষার্থীদেরকে আটকও করা হয়।</p>
                  <p>চট্টগ্রামে সাউন্ড গ্রেনেড ও কাঁদানে গ্যাসের শেল ছুড়ে বিক্ষোভকারীদের ছত্রভঙ্গ করে দেওয়া হয়েছে। কুমিল্লায় আন্দোলনে যোগ দিতে যাওয়ার সময় এক শিক্ষার্থী পায়ে গুলিবিদ্ধ হয়েছেন। কে বা কারা ওই ছাত্রকে গুলি করেছে, তা কেউ বলতে পারছে না। জাহাঙ্গীরনগর ও রাজশাহী বিশ্ববিদ্যালয়ে শিক্ষার্থীদের কর্মসূচিতে কয়েকজন শিক্ষকও যোগ দেন।</p>
                  <p>সহিংসতায় নিহত ব্যক্তিদের স্মরণে কাল মঙ্গলবার সারা দেশে <span class="clause-highlight">শোক পালন</span> করার সিদ্ধান্ত নেওয়া হয় মন্ত্রিসভার বৈঠকে।</p>
                  <p>সরকার ঘোষিত মঙ্গলবারের রাষ্ট্রীয় শোক প্রত্যাখ্যান করে বৈষম্য বিরোধী ছাত্র আন্দোলন। এর বদলে আজ একক বা ঐক্যবদ্ধভাবে লাল কাপড় মুখে ও চোখে বেঁধে ছবি তোলা এবং অনলাইনে প্রচার কর্মসূচির ঘোষণা করে।</p>

                  <p><span class="date-highlight">৩০ জুলাই, মঙ্গলবার</span> হত্যার বিচার চেয়ে মুখে লাল কাপড় বেঁধে মিছিল। জাতিসংঘ মহাসচিবের বিবৃতি, স্বচ্ছ তদন্তের আহ্বান। কোটা সংস্কার আন্দোলন ঘিরে সংঘর্ষে নিহত ব্যক্তিদের স্মরণে সামাজিক যোগাযোগমাধ্যম ফেসবুকের প্রোফাইল লাল রঙের ফ্রেমে রাঙিয়েছেন অনেকে।</p>
                  <p><span class="clause-highlight">‘মার্চ ফর জাস্টিস’</span> কর্মসূচি পালন করার ঘোষণা দেয় বৈষম্যবিরোধী ছাত্র আন্দোলন। । ৯ দফা দাবি আদায়ের লক্ষ্যে এ কর্মসূচি পালন করা হয় দেশের সব আদালত, ক্যাম্পাস এবং রাজপথে।</p>

                  <p><span class="date-highlight">৩১ জুলাই, বুধবার</span> মার্চ ফর জাস্টিস কর্মসূচির পর বৃহস্পতিবারের জন্য নতুন কর্মসূচি ঘোষণা করেছে কোটা সংস্কারের দাবিতে আন্দোলনকারী বৈষম্য বিরোধী ছাত্র আন্দোলনের একাংশ। এই দিনের কর্মসূচির নাম দেওয়া হয়েছে <span class="clause-highlight">‘রিমেম্বারিং আওয়ার হিরোস’</span>।</p>
                  <p>কর্মসূচি পালনের বিষয়ে বিজ্ঞপ্তিতে বলা হয়েছে, ‘নির্যাতনের ভয়ংকর দিন-রাতগুলোর স্মৃতিচারণ, শহীদ ও আহতদের নিয়ে পরিবার এবং সহপাঠীদের স্মৃতিচারণ, বৈষম্যবিরোধী ছাত্র আন্দোলনে হওয়া নির্যাতনের ঘটনা নিয়ে চিত্রাঙ্কন/গ্রাফিতি, দেয়াল লেখন, ফেস্টুন তৈরি, ডিজিটাল পোর্ট্রেট তৈরি প্রভৃতি।’</p>
                  <p>ঐদিন সাবেক ছাত্রলীগ নেতাদের তোপের মুখে পড়েন আওয়ামী লীগের সাধারণ সম্পাদক ওবায়দুল কাদের। সাবেক ছাত্রনেতাদের মতবিনিময়ের জন্য ডেকে কথা বলতে না দেওয়ায় এমন পরিস্থিতির সৃষ্টি হয়। এ সময় সাবেক ছাত্রনেতাদের কেউ কেউ ‘ভুয়া ভুয়া’ বলেও স্লোগান দেন।</p>

                  <p><span class="date-highlight">৩২শে জুলাই</span>, বৃহস্পতিবার (১লা আগস্ট) গোয়েন্দা পুলিশের (ডিবি) হেফাজতে থাকা বৈষম্যবিরোধী ছাত্র আন্দোলনের ছয় সমন্বয়ককে বেলা দেড়টার একটু পরেই ছেড়ে দেওয়া হয়।</p>
                  <p>ঐদিন জামায়াতে ইসলামী ও ইসলামী ছাত্রশিবিরকে নিষিদ্ধ করে প্রজ্ঞাপন জারি করে সরকার।</p>

                  <p><span class="date-highlight">৩৩শে জুলাই</span> (২রা আগস্ট) শুক্রবার বৈষম্যবিরোধী ছাত্র আন্দোলন <span class="clause-highlight">‘প্রার্থনা ও ছাত্র-জনতার গণমিছিল’</span> কর্মসূচি ঘোষণা করে। ৩৩শে জুলাই গোয়েন্দা পুলিশের (ডিবি) হেফাজত থেকে ছাড়া পাওয়া বৈষম্যবিরোধী আন্দোলনের ছয় সমন্বয়ক গণমাধ্যমে পাঠানো এক বিবৃতিতে বলেন যে আন্দোলন প্রত্যাহার করে ডিবি অফিস থেকে প্রচারিত ছয় সমন্বয়কের ভিডিও বিবৃতিটি তারা স্বেচ্ছায় দেননি।</p>
                </div>

