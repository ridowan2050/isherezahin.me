                  <p><span class="date-highlight">৯ জুন, ২০২৪, শুক্রবার</span> সরকারি চাকরিতে কোটাব্যবস্থা পুনর্বহালের প্রতিবাদে আবারও বিক্ষোভ সমাবেশ করেন ঢাকা বিশ্ববিদ্যালয়ের শিক্ষার্থীরা। শিক্ষার্থীরা তাঁদের দাবি মানতে সরকারকে ৩০ জুন পর্যন্ত সময় বেঁধে দেন। এই সময়ের মধ্যে দাবি পূরণ না হলে সর্বাত্মক আন্দোলনের ঘোষণা দেন তাঁরা। একই দাবিতে চট্টগ্রাম ও বরিশাল বিশ্ববিদ্যালয়ের শিক্ষার্থীরাও আন্দোলন কর্মসূচি পালন করেন। ‘ঢাকা বিশ্ববিদ্যালয়ের শিক্ষার্থীবৃন্দ’ ব্যানারে আয়োজিত কর্মসূচিতে বিপুলসংখ্যক শিক্ষার্থী অংশ নেন। এদিকে কোটা বাতিল-সংক্রান্ত হাইকোর্টের দেওয়া রায় স্থগিত চেয়ে রাষ্ট্রপক্ষের করা আবেদন আপিল বিভাগের পূর্ণাঙ্গ বেঞ্চে শুনানির জন্য ৪ জুলাই দিন নির্ধারণ করা হয়।</p>

                  <p><span class="date-highlight">১ জুলাই, সোমবার</span> কোটা বাতিলের পরিপত্র পুনর্বহালের দাবিতে ছাত্রসমাবেশ ও বিক্ষোভ অনুষ্ঠিত হয় ঢাকা বিশ্ববিদ্যালয়সহ দেশের বিভিন্ন বিশ্ববিদ্যালয়ে। ঢাকা বিশ্ববিদ্যালয়ে অনুষ্ঠিত সমাবেশে ৪ জুলাইয়ের মধ্যে দাবির বিষয়ে চূড়ান্ত সুরাহার আহ্বান জানানো হয়।</p>

                  <p><span class="date-highlight">২ জুলাই, মঙ্গলবার</span> আন্দোলনকারীরা এই দিন বেলা পৌনে তিনটার দিকে ঢাকা বিশ্ববিদ্যালয় কেন্দ্রীয় গ্রন্থাগারের সামনে থেকে মিছিল করে নীলক্ষেত, সায়েন্স ল্যাবরেটরি ও বাটা সিগন্যাল মোড় ঘুরে শাহবাগে গিয়ে থামে। সেখানে তাঁরা এক ঘণ্টা <span class="clause-highlight">অবরোধ কর্মসূচি</span> পালন করেন। একই দিন বিকেলে জাহাঙ্গীরনগর বিশ্ববিদ্যালয়ের একদল শিক্ষার্থী ঢাকা-আরিচা ২০ মিনিটের জন্য অবরোধ করেন।</p>

                  <p><span class="date-highlight">৩ জুলাই, বুধবার</span> এই দিন আন্দোলনকারীরা ঢাকার শাহবাগ মোড় দেড় ঘণ্টার মতো অবরোধ করে রাখেন। একই দাবিতে আরও ছয়টি বিশ্ববিদ্যালয়ের শিক্ষার্থীরা বিক্ষোভ ও অবরোধ করেন। এর মধ্যে ময়মনসিংহে রেললাইনে ট্রেন অবরোধ করে বিক্ষোভ করেন বাংলাদেশ কৃষি বিশ্ববিদ্যালয়ের শিক্ষার্থীরা, জাহাঙ্গীরনগর বিশ্ববিদ্যালয়ের শিক্ষার্থীরা ঢাকা-আরিচা মহাসড়ক, চট্টগ্রাম বিশ্ববিদ্যালয়ের শিক্ষার্থীরা চট্টগ্রাম-খাগড়াছড়ি মহাসড়ক এবং বরিশাল বিশ্ববিদ্যালয়ের শিক্ষার্থীরা বরিশাল-কুয়াকাটা মহাসড়ক অবরোধ করে বিক্ষোভ করেন। আর জগন্নাথ বিশ্ববিদ্যালয়ের চাকরিপ্রত্যাশী শিক্ষার্থীরা পুরান ঢাকার তাঁতীবাজার মোড়ে সড়ক অবরোধ করেন। বেলা তিনটা থেকে সাড়ে তিনটা পর্যন্ত এই অবরোধ কর্মসূচি পালন করা হয়।</p>

                  <p><span class="date-highlight">৪ জুলাই, বৃহস্পতিবার</span> সরকারি চাকরিতে সরাসরি নিয়োগের ক্ষেত্রে মুক্তিযোদ্ধা কোটা বাতিলের পরিপত্র অবৈধ ঘোষণার বিষয়ে হাইকোর্টের রায় আপিল বিভাগে স্থগিত করেনি। প্রধান বিচারপতি ওবায়দুল হাসানের নেতৃত্বাধীন ছয় সদস্যের আপিল বিভাগ <span class="clause-highlight">‘নট টুডে’</span> বলে আদেশ দেন। পরের সপ্তাহে এ বিষয়ে শুনানি হতে পারে বলে ওই দিন অ্যাটর্নি জেনারেল কার্যালয় থেকে জানানো হয়। এ দিন শিক্ষার্থীরা দেশের বিভিন্ন মহাসড়ক অবরোধ করে। আর ঢাকায় শাহবাগ মোড় অবরোধ করে রাখে ৫ ঘণ্টা। এ দিন ছাত্র সমাবেশ থেকে ছাত্র ধর্মঘটের ডাক দেওয়া হয়।</p>

                  <p><span class="date-highlight">৫ জুলাই, শুক্রবার</span> ছুটির দিন শুক্রবারেও বিক্ষোভ ও চট্টগ্রাম, খুলনা ও গোপালগঞ্জে সড়ক অবরোধ করে আন্দোলনকারীরা।</p>

                  <p><span class="date-highlight">৬ জুলাই, শনিবার</span> দেশের বিভিন্ন বিশ্ববিদ্যালয়ে আগের দিনের মতোই বিক্ষোভ ও অবরোধ কর্মসূচি পালন করা হয়। এ সময় আন্দোলনকারীরা সব বিশ্ববিদ্যালয় ও কলেজে ক্লাস-পরীক্ষা বর্জন, ছাত্র ধর্মঘট এবং সারা দেশে সড়ক-মহাসড়ক অবরোধের ডাক দেন। এর নাম দেওয়া হয় <span class="clause-highlight">‘বাংলা ব্লকেড’</span>।</p>

                  <p><span class="date-highlight">৭ জুলাই, রোববার</span> বাংলা ব্লকেডে স্থবির রাজধানী। অনির্দিষ্টকালের জন্য বিশ্ববিদ্যালয় ও কলেজে ক্লাস ও পরীক্ষা বর্জনের ঘোষণা।</p>

                  <p><span class="date-highlight">৮ জুলাই, সোমবার</span> ঢাকার ১১টি স্থানে অবরোধ, ৯টি বিশ্ববিদ্যালয়ে বিক্ষোভ, ৩টি স্থানে রেলপথ অবরোধ এবং ৬টি মহাসড়ক অবরোধ। সরকারি চাকরিতে সব গ্রেডে অযৌক্তিক ও বৈষম্যমূলক কোটা বাতিল করে শুধু সংবিধান অনুযায়ী অনগ্রসর জনগোষ্ঠীর জন্য ন্যূনতম কোটা রেখে সংসদে আইন পাসের দাবি।</p>                  
                  <p>কোটাবৈষম্যের বিরুদ্ধে ও স্থায়ী সমাধানের দাবিতে <span class="clause-highlight">‘বৈষম্যবিরোধী ছাত্র আন্দোলন’</span> নামে ৬৫ সদস্যের <span class="clause-highlight">সমন্বয়ক টিম</span> গঠন।</p>
                  <p>সমন্বয়ক কমিটির সদস্য নাহিদ ইসলাম চার দফা দাবির পরিবর্তে এক দফা দাবির কথা বলেন। দাবিটি হলো, সরকারি চাকরিতে সব গ্রেডে অযৌক্তিক ও বৈষম্যমূলক কোটা বাতিল করে শুধু সংবিধান অনুযায়ী অনগ্রসর জনগোষ্ঠীর জন্য ন্যূনতম কোটা রেখে সংসদে আইন পাস করা। নাহিদ এ সময় আরও বলেন,</p>
                  <blockquote>
                    <p>“আমরা আমাদের এক দফা দাবি দিয়ে দিয়েছি, যেখানে আদালতের কোনো এখতিয়ার নেই। এটা করার দায়িত্ব কেবল নির্বাহী বিভাগ ও সরকারের। ফলে বল এখন সরকারের কোর্টে। এখন আর আদালত দেখিয়ে কোনো লাভ নেই। সরকারই ঠিক করতে পারে, এই আন্দোলনের গতিপথ কী হবে।”</p>
                  </blockquote>

                  <p><span class="date-highlight">৯ জুলাই, মঙ্গলবার</span> হাইকোর্টের রায় স্থগিত চেয়ে দুই শিক্ষার্থীর আবেদন। শুনানি হবে পরদিন। অবশ্য ওই দুই শিক্ষার্থীর আবেদন করার সঙ্গে কোটাবিরোধী আন্দোলনকারীদের কোনো সম্পর্ক নেই বলে দাবি করে বৈষম্যবিরোধী ছাত্র আন্দোলন।</p>
                  <p>আন্দোলনের অংশ হিসেবে শিক্ষার্থী ও চাকরিপ্রত্যাশীরা চার ঘণ্টা পর অবরোধ তুলে নেন। আন্দোলনকারীদের পক্ষ থেকে পরদিন সারা দেশে সকাল-সন্ধ্যা <span class="clause-highlight">‘বাংলা ব্লকেড’</span> নামে অবরোধ কর্মসূচির ঘোষণা দেওয়া হয়।</p>

                  <p><span class="date-highlight">১০ জুলাই, বুধবার</span> সরকারি চাকরিতে সরাসরি নিয়োগে (৯ম থেকে ১৩তম গ্রেড) কোটার বিষয়ে পক্ষগুলোকে চার সপ্তাহের জন্য স্থিতাবস্থা বজায় রাখতে নির্দেশ আপিল বিভাগের। প্রধান বিচারপতি ওবায়দুল হাসানের নেতৃত্বাধীন পাঁচ সদস্যের আপিল বিভাগ এ আদেশ দেন। শুনানির জন্য আগামী ৭ আগস্ট দিন রাখা হয়।</p>
                  <p>অন্যদিকে সরকারি চাকরির সব পদে কোটা সংস্কারের দাবি করেন আন্দোলনকারী শিক্ষার্থী ও চাকরিপ্রত্যাশীরা। তাঁরা মনে করছেন, এটি সরকারের সিদ্ধান্তের বিষয়।</p>

                  <p><span class="date-highlight">১১ জুলাই, বৃহস্পতিবার</span> আওয়ামী লীগের সাধারণ সম্পাদক ওবায়দুল কাদের বলেন, কোটাবিরোধী আন্দোলনকারীরা সর্বোচ্চ আদালতের বিরুদ্ধে শক্তি প্রদর্শন করছেন। এটি অনভিপ্রেত ও সম্পূর্ণ বেআইনি।</p>
                  <p>সচিবালয়ে এক সংবাদ সম্মেলনে স্বরাষ্ট্রমন্ত্রী আসাদুজ্জামান খান বলেন, শিক্ষার্থীরা ‘লিমিট ক্রস’ করে যাচ্ছেন।</p>
                  <p>এ দিন পুলিশের বাধার মুখেই দেশের বিভিন্ন স্থানে অবরোধ পালন করেন আন্দোলনকারীরা।</p>

                  <p><span class="date-highlight">১২ জুলাই, শুক্রবার</span> কোটা সংস্কারের দাবিতে শুক্রবার ছুটির দিনেও দেশের বিভিন্ন বিশ্ববিদ্যালয় ও কলেজ ক্যাম্পাসে বিক্ষোভ করেন শিক্ষার্থীরা। ঢাকা বিশ্ববিদ্যালয়ে মিছিল শেষে শিক্ষার্থীরা শাহবাগ মোড় অবরোধ করেন। রেলপথ অবরোধ করেন রাজশাহী বিশ্ববিদ্যালয়ের শিক্ষার্থীরা।</p>

                  <p><span class="date-highlight">১৩ জুলাই, শনিবার</span> সব গ্রেডে কোটার যৌক্তিক সংস্কারের দাবিতে রাষ্ট্রপতির কাছে স্মারকলিপি দেওয়ার কর্মসূচি ঘোষণা।</p>

                  <p><span class="date-highlight">১৪ জুলাই, রোববার</span> গণভবনে অনুষ্ঠিত সংবাদ সম্মেলনে প্রধানমন্ত্রী শেখ হাসিনা প্রশ্নের জবাবে কোটা আন্দোলনকারীদের “রাজাকারের নাতি-পুতি” হিসেবে অভিহিত করেন,</p>
                  <blockquote>
                    <p>মুক্তিযোদ্ধাদের নাতি-পুতিরাও পাবে না? তাহলে কি রাজাকারের নাতি-পুতিরা পাবে?</p>
                    <p>যত রাজাকারের বাচ্চারা-নাতি-পুতিরা হলো মেধাবী, তাই না?</p>
                  </blockquote>
                  <p>২০১৮ সালের আন্দোলন প্রসঙ্গে প্রধানমন্ত্রী জানান, সে সময়ে তিনি বিরক্ত হয়ে কোটা বাতিল করে দিয়েছিলেন। উদ্দেশ্য ছিল কোটা বাতিল হলে কী হয় সেটি দেখা।</p>
                  <p>একই দিনে পদযাত্রা কর্মসূচির মধ্য দিয়ে রাষ্ট্রপতির কাছে স্মারকলিপি দিয়ে আন্দোলনকারীরা জাতীয় সংসদে জরুরি অধিবেশন ডেকে সরকারি চাকরির সব গ্রেডের কোটার যৌক্তিক সংস্কারের দাবিতে সরকারকে ২৪ ঘণ্টার সময় বেঁধে দেন।</p>
                  <p>পরে মধ্যরাতে সরকারি চাকরিতে কোটাব্যবস্থা সংস্কারের দাবিতে আন্দোলনকারীদের অবমাননা করা হয়েছে দাবি করে ঢাকা বিশ্ববিদ্যালয় ক্যাম্পাসে বিক্ষোভ করেন শিক্ষার্থীরা। চট্টগ্রাম বিশ্ববিদ্যালয়ে শিক্ষার্থীরা বিক্ষোভ শুরু করলে হামলা চালায় ছাত্রলীগ। মিছিল হয়েছে জাহাঙ্গীরনগর ও জগন্নাথ বিশ্ববিদ্যালয়েও।</p>
                  <p>রাত ১০টা থেকে ঢাকা বিশ্ববিদ্যালয়ের বিভিন্ন আবাসিক হলে, তুমি কে? আমি কে? রাজাকার, রাজাকার; স্লোগান দিয়ে বিক্ষোভে করে। বিক্ষোভের পর মধ্যরাতে শিক্ষার্থীরা জড়ো হন টিএসসির রাজু ভাস্কর্যে।</p>
                  <p>সেখানে প্রধানমন্ত্রীর মন্তব্যের প্রতিক্রিয়া হিসেবে আন্দোলনকারী শিক্ষার্থীরা ব্যঙ্গ করে স্লোগান দেয়,</p>
                  <blockquote>
                    <p>“তুমি কে? আমি কে? রাজাকার, রাজাকার; কে বলেছে? কে বলেছে? স্বৈরাচার, স্বৈরাচার” এবং “চাইতে গেলাম অধিকার; হয়ে গেলাম রাজাকার”</p>
                  </blockquote>

                  <p><span class="date-highlight">১৫ জুলাই, সোমবার</span> বেলা দুইটায় রাজধানীর ধানমন্ডিতে আওয়ামী লীগ সভাপতির রাজনৈতিক কার্যালয়ে আয়োজিত সংবাদ সম্মেলনে দুপুর দুইটায় কোটা সংস্কারের দাবিতে আন্দোলনকারীদের উদ্দেশ্য করে বলেন,</p>
                  <blockquote>
                    <p>“আন্দোলন থেকে আত্মস্বীকৃত রাজাকার ও ঔদ্ধত্যপূর্ণ মানসিকতা বা আচরণের প্রকাশ ঘটেছে। এর জবাব দেওয়ার জন্য ছাত্রলীগ প্রস্তুত।”</p>
                  </blockquote>

                  <p>এর পরে দুপুরে কোটা সংস্কার আন্দোলনে যাঁরা ‘আমি রাজাকার’ স্লোগান দিচ্ছেন, তাঁদের শেষ দেখিয়ে ছাড়বেন বলে মন্তব্য করেন বাংলাদেশ ছাত্রলীগের কেন্দ্রীয় সভাপতি সাদ্দাম হোসেন।</p>
                  <p>বেলা তিনটায় ঢাকা বিশ্ববিদ্যালয়ে দফায় দফায় ছাত্রলীগ ও আন্দোলনকারীদের মধ্যে সংঘর্ষ বাধে। এ সময় আন্দোলনকারীদের মারধর করা হয়। ছাত্রলীগ এবং আওয়ামী যুবলীগ এর হাতে গুলি এবং গুলি করতেও দেখা যায়। দেশের বিভিন্ন স্থানে হামলা হয়। আহত ২৯৭ জন ঢাকা মেডিকেলে চিকিৎসা নেন।</p>
                  <p>ছাত্রলীগের নেতাকর্মীরা ঢাকা মেডিকেল কলেজ হাসপাতালে গিয়ে চিকিৎসারত ছাত্রদের উপর হামলা করে।</p>
                  <p>এই দিনের ছাত্রলীগের হামলার বেশ কিছু ভিডিও ফুটেজ এবং ছবি সামাজিক যোগাযোগ মাধ্যমে ছড়িয়ে পরলে সারাদেশের পরিস্থিতি সাথে সাথে পাল্টে যায়, আন্দোলন ভয়ংকর রূপ ধারণ করে।</p>

                  <p><span class="date-highlight">১৬ জুলাই, মঙ্গলবার</span> সারা দেশে দিনভর ব্যাপক বিক্ষোভ ও সংঘর্ষ। আন্দোলনকারীদের ওপর হামলা করে ছাত্রলীগ, যুবলীগসহ সরকার সমর্থকেরা। এতে নিহত হন <span class="clause-highlight">ছয়জন</span>। রংপুরে আন্দোলনকারী বেগম রোকেয়া বিশ্ববিদ্যালয়ের শিক্ষার্থী <span class="clause-highlight">আবু সাঈদ</span> পুলিশের বুলেটে নিহত হওয়ার সচিত্র ছবি প্রকাশ।</p>
                  <p>নিহত ব্যক্তিদের স্মরণে আন্দোলনকারী শিক্ষার্থীদের নতুন কর্মসূচি বুধবার <span class="clause-highlight">গায়েবানা জানাজা ও কফিন মিছিল</span> করবেন বলে ঘোষণা দেওয়া হয়।</p>

                  <p><span class="date-highlight">১৭ জুলাই, বুধবার</span> ঢাকা বিশ্ববিদ্যালয়সহ দেশের বিভিন্ন বিশ্ববিদ্যালয় থেকে ছাত্রলীগের নেতা-কর্মীদের বিতাড়ন করে <span class="clause-highlight">‘রাজনীতিমুক্ত’</span> ঘোষণা দেওয়া হয়।</p>
                  <p>সেদিন সন্ধ্যা ৭টা ৫৫ মিনিটে বৈষম্যবিরোধী ছাত্র আন্দোলনের ফেসবুক পেজে পোস্টের মাধ্যমে কমপ্লিট শাটডাউন ঘোষণা দেয়া হয়। পোস্টে বলা হয়েছে, </p>
                  <blockquote>
                    <p>“কোটা সংস্কার আন্দোলনকারীদের ওপর পুলিশ, বিজিবি, র‍্যাব, সোয়াটের ন্যক্কারজনক হামলা, খুনের প্রতিবাদ, খুনিদের বিচার, সন্ত্রাসমুক্ত ক্যাম্পাস নিশ্চিত ও এক দফা দাবিতে ১৮ জুলাই সারাদেশে ‘কমপ্লিট শাটডাউন’ ঘোষণা করছি। শুধুমাত্র হাসপাতাল ও জরুরি সেবা ছাড়া কোনো প্রতিষ্ঠানের দরজা খুলবে না, অ্যাম্বুলেন্স ছাড়া সড়কে কোনো গাড়ি চলবে না। সারাদেশের প্রতিটি স্কুল, কলেজ, বিশ্ববিদ্যালয়, প্রাইভেট বিশ্ববিদ্যালয়, মাদ্রাসা শিক্ষার্থীদের আহ্বান জানাচ্ছি, আগামীকালকের কর্মসূচি সফল করুন। আমাদের অভিভাবকদের বলছি, আমরা আপনাদেরই সন্তান। আমাদের পাশে দাঁড়ান, রক্ষা করুন। এই লড়াইটা শুধু ছাত্রদের না, দলমত নির্বিশেষে এদেশের আপামর জনসাধারণের।”</p>
                  </blockquote>

                  <p><span class="date-highlight">১৮ জুলাই, বৃহস্পতিবার</span> দেশব্যাপী প্রতিরোধ, সহিংসতা, সংঘর্ষ ও গুলি। মোট নিহত ২৭ জন। আন্দোলনকারী শিক্ষার্থীদের সর্বাত্মক অবরোধ কর্মসূচিকে কেন্দ্র করে ঢাকাসহ সারা দেশ ছিল প্রায় অচল। রাজধানী ছাড়াও দেশের ৪৭টি জেলায় দিনভর বিক্ষোভ, অবরোধ, পাল্টাপাল্টি ধাওয়া, পুলিশের হামলা-গুলি ও সংঘাতের ঘটনা ঘটে। এসব ঘটনায় আহত হয়েছেন অন্তত দেড় হাজার। কোথাও কোথাও আন্দোলনকারীদের সঙ্গে আইনশৃঙ্খলা রক্ষাকারী বাহিনীর সদস্যদের, আবার কোথাও সরকার-সমর্থক বিভিন্ন সংগঠনের নেতা-কর্মীদের সংঘর্ষ হয়।</p>
                  <p>ঐদিনই সারা দেশে বিজিবি মোতায়েন করা হয়।</p>

                  <p><span class="date-highlight">১৯ জুলাই, শুক্রবার</span> কোটা সংস্কারের দাবিতে আন্দোলনরত শিক্ষার্থীদের <span class="clause-highlight">‘কমপ্লিট শাটডাউন’</span> বা সর্বাত্মক অবরোধের কর্মসূচি ঘিরে রাজধানী ঢাকায় ব্যাপক সংঘর্ষ, হামলা, ভাঙচুর, গুলি, অগ্নিসংযোগ ও প্রাণহানির ঘটনা ঘটে। রাজধানী ঢাকা ছিল কার্যত অচল, পরিস্থিতি ছিল থমথমে। দেশের বিভিন্ন জেলাতেও ব্যাপক বিক্ষোভ, সংঘর্ষ ও সহিংসতা হয়।</p>
                  <p>এদিন রাজধানীর বিভিন্ন এলাকায় গুলি ও সংঘর্ষে অন্তত ৪৪ জন নিহত হন। শুরু থেকে এ আন্দোলনে ছিলেন শুধু শিক্ষার্থীরা। তবে গতকাল রাজধানীর বিভিন্ন এলাকায় স্থানীয় লোকজনকেও অংশ নিতে দেখা গেছে।</p>
                  <p>আন্দোলনকারীরা জানান, ৯ দফা দাবি না মানা পর্যন্ত চলবে ‘শাটডাউন’ কর্মসূচি অব্যাহত থাকবে। আন্দোলনকারী শিক্ষার্থীদের নয় দফা দাবির মধ্যে রয়েছে—</p>
                </div>

