                  <p>স্লোগান দেয়।</p>

                  <p><span class="date-highlight">১৫ জুলাই</span> এর পরেরদিন আওয়ামী লীগ ও তৎকালীন সরকারের বিভিন্ন কর্মকর্তা, মন্ত্রী আন্দোলনকারী শিক্ষার্থীদের বিরুদ্ধে ‘মুক্তিযুদ্ধের চেতনা’ নষ্ট করার অভিযোগ আনেন। ১৫ জুলাই বৈষম্য বিরোধী ছাত্র আন্দোলনের অন্যতম সমন্বয়ক নাহিদ ইসলাম বলেন, “গত রাতে বিক্ষোভ করে আমরা সোমবার ১২টার মধ্যে প্রধানমন্ত্রীকে তার বক্তব্য প্রত্যাহারের দাবি জানিয়েছিলাম। প্রত্যাহার না হওয়ায় আমরা রাস্তায় নেমেছি।” পরবর্তী দিনগুলিতে বৈষম্যবিরোধী ছাত্র আন্দোলনে নেতৃত্বে দেশের বিভিন্ন স্থানে আন্দোলন চলে।</p>

                  <p><span class="date-highlight">১৭ জুলাই</span> রাতে তারা ১৮ জুলাইয়ের জন্য ‘কমপ্লিট শাটডাউন’ কর্মসূচির ঘোষণা করে।</p>

                  <p><span class="date-highlight">১৮ জুলাই</span> সারাদেশে ‘কমপ্লিট শাটডাউন’ কর্মসূচি পালিত হয়।</p>

                  <p><span class="date-highlight">১৯ জুলাইতেও</span> সারাদেশে বৈষম্যবিরোধী ছাত্র আন্দোলনের ডাকা সর্বাত্মক অবরোধ চলে। এদিন মধ্যরাতে বৈষম্যবিরোধী ছাত্র আন্দোলনের অন্যতম কেন্দ্রীয় সমন্বয়ক নাহিদ ইসলামকে আটক করা হয়। যেসময়ে নাহিদ ইসলামকে আটক করা হয়, তার কাছাকাছি সময়ে বৈষম্যবিরোধী ছাত্র আন্দোলনের তিনজন প্রতিনিধির সাথে সরকারের তিনজন প্রতিনিধির একটি বৈঠক অনুষ্ঠিত হয় যেখানে তারা সরকারের কাছে 'আট দফা দাবি' জানান। বৈষম্যবিরোধী ছাত্র আন্দোলনের পক্ষে আলোচনায় অংশ নেন সমন্বয়ক সারজিস আলম ও হাসনাত আব্দুল্লাহ এবং সহ-সমন্বয়ক তানভীর আহমেদ।</p>
                  
                  <p><span class="date-highlight">২০ জুলাই</span> সরকারের তিনজন মন্ত্রীর সাথে যে বৈঠক অনুষ্ঠিত হয়, সেই বৈঠক এবং বৈঠকে উত্থাপিত দাবিগুলো নিয়ে নেতৃত্বের মধ্যে মতবিরোধ দেখা যায়। আন্দোলনের অন্যতম সমন্বয়ক জাহাঙ্গীরনগর বিশ্ববিদ্যালয়ের শিক্ষার্থী আরিফ সোহেল অভিযোগ করে বলেন,</p> 
                  <blockquote>
                    <p>“তারা [গতকাল রাতে মন্ত্রীদের সাথে বৈঠক করা তিন প্রতিনিধি] বৈষম্যবিরোধী ছাত্র আন্দোলনের একটি অংশও না। তারা বৈষম্যবিরোধী ছাত্র আন্দোলনের সাথে নেই। তারা যদি এই সিদ্ধান্ত নিয়ে থাকেন এবং বৈষম্যবিরোধী ছাত্র আন্দোলনের নামে প্রচার করেন, তবে তারা মিথ্যাচার করছেন।” অন্য আরেক সমন্বয়ক আবদুল কাদের বলেন, “কয়েকজন সমন্বয়ক ও সহ-সমন্বয়ককে দিয়ে জোরপূর্বক গণমাধ্যমে ভুল সংবাদ প্রচারের চেষ্টা করা হচ্ছে।”</p>
                  </blockquote>

