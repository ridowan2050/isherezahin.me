                  <p><span class="date-highlight">২২ জুলাই</span> বৈষম্যবিরোধী ছাত্র আন্দোলনের অন্যতম সমন্বয়ক নাহিদ ইসলাম <span class="clause-highlight">চার দফা</span> দাবি জানিয়ে ৪৮ ঘণ্টার আল্টিমেটাম দিয়ে <span class="clause-highlight">‘কমপ্লিট শাটডাউন’</span> কর্মসূচি স্থগিত করেন। তিনি জানান,</p>
