                <div id="category5">
                  <h3  id="কারফিউ জারি">কারফিউ জারি</h3>
                  <p>রাতে সারা দেশে কারফিউ জারি, সেনাবাহিনী মোতায়েন এবং ইন্টারনেট সেবা সম্পূর্ণ বন্ধ করা হয়।</p>

                  <p><span class="date-highlight">২০ জুলাই, শনিবার</span> দেশজুড়ে কারফিউ, সেনা মোতায়েন। সাধারণ ছুটি ঘোষণা। রাজধানীর বিভিন্ন স্থানে সংঘর্ষ, ধাওয়া ও গুলি। উল্লেখযোগ্য স্থান হচ্ছে, যাত্রাবাড়ী, উত্তরা, বাড্ডা ও মিরপুর। এ ছাড়া মোহাম্মদপুরেও সংঘর্ষের ঘটনা ঘটে। সংঘর্ষে শনিবার নিহত ২৬।</p>
                  <p>স্বরাষ্ট্রমন্ত্রী বলেন, পরিস্থিতি স্বাভাবিক না হওয়া পর্যন্ত কারফিউ চলবে। ঐদিন ‘বৈষম্যবিরোধী ছাত্র আন্দোলনের’ অন্যতম সমন্বয়ক নাহিদ ইসলামকে তুলে নেওয়া হয়।</p>
                  <p>আন্দোলনের তিন সমন্বয়ক তিন মন্ত্রীর সঙ্গে দেখা করে আট দফা দাবি পেশ।</p>
                  <p>মন্ত্রীদের কাছে দেওয়া আট দফা দাবির মধ্যে রয়েছে নিহতদের ঘটনা তদন্ত করে দায়ী ব্যক্তিদের বিরুদ্ধে হত্যা মামলা করে গ্রেপ্তার ও বিচার। শহীদদের পরিবারকে আর্থিক সহযোগিতা, মাসিক ভাতা ও তাঁদের পিতামাতার মতামতের ভিত্তিতে একজন সদস্যকে চাকরির নিশ্চয়তা দেওয়া। সব পাবলিক বিশ্ববিদ্যালয়ের আবাসিক হলগুলোতে প্রশাসনিকভাবে সিট বরাদ্দ, সন্ত্রাসী কার্যক্রম বন্ধ করা এবং ছাত্র সংসদ চালু করা, কোটা সংস্কার আন্দোলনকারীদের বিরুদ্ধে করা সব মামলা প্রত্যাহার, কোটা সংস্কার আন্দোলনের সঙ্গে জড়িত সব শিক্ষার্থীকে সব ধরনের রাজনৈতিক, আইনি বা বিশ্ববিদ্যালয়ের প্রশাসনের মাধ্যমে একাডেমিক হয়রানি না করার নিশ্চয়তা দেওয়া।</p>
                  <p>একইদিনে কাজীপাড়া ও মিরপুর-১০ নম্বর মেট্রো স্টেশনে বিপুল ক্ষতি হয়। গত শুক্রবার সরকারি তিন স্থাপনায় আগুনে ১১৩ যানবাহন পুড়ে ছাই হওয়ার খবরও পাওয়া যায়।</p>

                  <p><span class="date-highlight">২১ জুলাই, রোববার</span> সরকারি চাকরিতে নিয়োগের ক্ষেত্রে কোটা পুনর্বহাল-সংক্রান্ত হাইকোর্টের রায় সামগ্রিকভাবে বাতিল (রদ ও রহিত) করে দেশের সর্বোচ্চ আদালতের রায় প্রদান। রায়ে বলা হয়, কোটাপ্রথা হিসেবে মেধাভিত্তিক ৯৩ শতাংশ; মুক্তিযোদ্ধা, শহীদ মুক্তিযোদ্ধা ও বীরাঙ্গনার সন্তানদের জন্য ৫ শতাংশ; ক্ষুদ্র নৃগোষ্ঠী ১ শতাংশ এবং প্রতিবন্ধী ও তৃতীয় লিঙ্গের জন্য ১ শতাংশ নির্ধারণ করা হলো। তবে নির্ধারিত কোটায় যোগ্য প্রার্থী না পাওয়া গেলে সংশ্লিষ্ট কোটার শূন্য পদগুলো সাধারণ মেধাতালিকা থেকে পূরণ করতে হবে। এই নির্দেশনার আলোকে সরকারের নির্বাহী বিভাগকে অনতিবিলম্বে প্রজ্ঞাপন জারি করতে নির্দেশ দেন আপিল বিভাগ।</p>
                  <p>এদিকে আন্দোলনের সমন্বয়ক নাহিদকে তুলে নিয়ে করে হাসপাতালে ভর্তি করা হয়।</p>
                  <p>বৈষম্যবারোধী ছাত্র আন্দোলনের সমন্বয়কেরা নতুন করে চার দফা দাবি পূরণের জন্য ৪৮ ঘন্টা সময় বেঁধে দিলেন।</p>
