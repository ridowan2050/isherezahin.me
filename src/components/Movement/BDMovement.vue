                    <li>ঢাকা বিশ্ববিদ্যালয়, জাহাঙ্গীরনগর বিশ্ববিদ্যালয়, চট্টগ্রাম বিশ্ববিদ্যালয় ও রাজশাহী বিশ্ববিদ্যালয়সহ সকল শিক্ষা প্রতিষ্ঠানে ছাত্রলীগসহ দলীয় লেজুরবৃত্তিক ছাত্র রাজনীতি নিষিদ্ধ করে ছাত্র সংসদকে কার্যকর করতে হবে। ৭. অবিলম্বে সকল শিক্ষাপ্রতিষ্ঠান ও হলসমূহ খুলে দিতে হবে।</li>
                    <li>আর যে সকল ছাত্র কোটা সংস্কার আন্দোলনে অংশগ্রহণ করেছেন, তাঁদের কোনো ধরনের একাডেমিক ও প্রশাসনিক হয়রানি না করার নিশ্চয়তা প্রদান করতে হবে।</li>
                    <li>প্রধানমন্ত্রী শেখ হাসিনাকে ছাত্র হত্যার দায় নিয়ে প্রকাশ্যে ক্ষমা চাইতে হবে।</li>
                  </ul>
                  <p>সর্বাত্মক অবরোধের কর্মসূচি ঘিরে গত তিন দিনে সারা দেশে নিহত হন ১০৩ জন।</p>
