                  <h3  id="অসহযোগ কর্মসূচি">অসহযোগ কর্মসূচি</h3>
                  <p><span class="date-highlight">৩৪শে জুলাই</span>(৩রা আগস্ট) অনির্দিষ্টকালের জন্য সর্বাত্মক অসহযোগ আন্দোলনের ডাক দেয়া হয়। কেন্দ্রীয় শহীদ মিনারে দেয়া হয় অসহযোগ আন্দোলনের রূপরেখা। রূপরেখাগুলো ছিল:</p>
                  <ul>
                    <li>কেউ কোনো ধরনের ট্যাক্স বা খাজনা প্রদান করবেন না।</li>
                    <li>বিদ্যুৎ বিল, গ্যাস বিল, পানির বিলসহ কোনো ধরনের বিল পরিশোধ করবেন না।</li>
                    <li>সকল ধরনের সরকারি বেসরকারি প্রতিষ্ঠান, অফিস আদালত ও কল কারখানা বন্ধ থাকবে। আপনারা কেউ অফিসে যাবেন না, মাস শেষে বেতন তুলবেন।</li>
                    <li>শিক্ষা প্রতিষ্ঠানসমূহের কার্যক্রম বন্ধ থাকবে।</li>
                    <li>প্রবাসীরা ব্যাংকিং চ্যানেলে কোনো ধরনের রেমিটেন্স দেশে পাঠাবেন না।</li>
                    <li>সকল ধরনের সরকারি সভা, সেমিনার, আয়োজন বর্জন করবেন।</li>
                    <li>বন্দরের কর্মীরা কাজে যোগ দেবেন না। কোনো ধরনের পণ্য খালাস করবেন না।</li>
                    <li>দেশের কোনো কলকারখানা চলবে না, গার্মেন্টকর্মী ভাই বোনেরা কাজে যাবেন না।</li>
                    <li>গণপরিবহন বন্ধ থাকবে। শ্রমিকরা কেউ কাজে যাবেন না।</li>
                    <li>জরুরি ব্যক্তিগত লেনদেনের জন্য প্রতি সপ্তাহের রোববার ব্যাংকগুলো খোলা থাকবে।</li>
