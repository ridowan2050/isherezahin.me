                  <p><span class="date-highlight">২০২৪ সালের ১ জুলাই</span> সংগঠনটি সৃষ্টি হয়। বাংলাদেশে কোটা সংস্কারের লক্ষ্যে ২০২৪ সালের ১ জুলাই থেকে চার দফা দাবিতে ‘বৈষম্যবিরোধী ছাত্র আন্দোলন’-এর ব্যানারে আন্দোলনকারী শিক্ষার্থীরা লাগাতার কর্মসূচি দেয়।</p>

                  <p><span class="date-highlight">২ থেকে ৬ জুলাই</span> দেশের বিভিন্ন স্থানে বিভিন্ন প্রতিষ্ঠানের শিক্ষার্থীরা বিক্ষোভ, মানববন্ধন, মহাসড়ক অবরোধ ইত্যাদি কর্মসূচি পালন করে।</p>

                  <p><span class="date-highlight">৭ জুলাই</span> বৈষম্যবিরোধী ছাত্র আন্দোলন ঢাকায় গণপরিবহন বন্ধ এবং রাস্তা অবরোধ কর্মসূচি চালায় এবং পরবর্তীতে সারাদেশে অবরোধ কর্মসূচি দেওয়া হয় যা "বাংলা ব্লকেড" কর্মসূচি নামে পরিচিত। বাংলা ব্লকেড চলাকালীন রাজধানীতে শুধু মেট্রো রেল চালু ছিল। পরবর্তী দিনগুলিতেও ঢাকাসহ দেশের বিভিন্ন স্থানে একই কর্মসূচিত পালিত হয়। এইসব কর্মসূচিতে শিক্ষার্থীরা ছাত্রলীগ ও পুলিশি হামলার শিকার হয়।</p>

                  <p><span class="date-highlight">১৪ জুলাই</span> আন্দোলনরত শিক্ষার্থীরা ঢাকায় গণপদযাত্রা করে রাষ্ট্রপতি মো. সাহাবুদ্দিন বরাবর স্মারকলিপি প্রদান করে। এদিন তৎকালীন প্রধানমন্ত্রী শেখ হাসিনা তার এক বক্তব্যে কোটা আন্দোলনকারীদের “রাজাকারের নাতি-পুতি” হিসেবে অভিহিত করেন,</p>
                  <blockquote>
                    <p>মুক্তিযোদ্ধাদের নাতি-পুতিরাও পাবে না? তাহলে কি রাজাকারের নাতি-পুতিরা পাবে?</p>
                    <p>শেখ হাসিনা এ সময় প্রশ্ন রেখে বলেন, যত রাজাকারের বাচ্চারা-নাতি-পুতিরা হলো মেধাবী, তাই না?</p>
                  </blockquote>
                  <p>প্রধানমন্ত্রীর মন্তব্যের প্রতিক্রিয়া হিসেবে আন্দোলনকারী শিক্ষার্থীরা ব্যঙ্গ করে</p>
                  <blockquote>
                    <p>“তুমি কে? আমি কে? রাজাকার, রাজাকার; কে বলেছে? কে বলেছে? স্বৈরাচার, স্বৈরাচার” এবং “চাইতে গেলাম অধিকার; হয়ে গেলাম রাজাকার”</p>
