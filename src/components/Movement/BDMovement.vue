                  </blockquote>

                  <p><span class="date-highlight">২৪ জুলাই</span> ১৯ জুলাই থেকে নিখোঁজ থাকার পর বৈষম্যবিরোধী ছাত্র আন্দোলনের অন্যতম সমন্বয়ক আসিফ মাহমুদ, আবু বাকের মজুমদার, এবং রিফাত রশীদের খোঁজ পাওয়া যায়। <span class="date-highlight">২৫ জুলাই</span> বৈষম্যবিরোধী ছাত্র আন্দোলনের পক্ষ থেকে আন্দোলনকারী শিক্ষার্থীদের উদ্দেশে আটটি বার্তা দেওয়া হয়। এর মধ্যে রয়েছে হতাহতদের তালিকা তৈরি, হত্যা ও হামলায় জড়িতদের চিহ্নিত করা, বিশ্ববিদ্যালয় ও হল খুলে দেওয়ার চাপ তৈরি করা।</p>
                  <p><span class="date-highlight">২৬ জুলাই</span> নাহিদ ইসলামসহ কোটা সংস্কার আন্দোলনের তিন সমন্বয়ককে রাজধানীর গণস্বাস্থ্য নগর হাসপাতাল থেকে তুলে নিয়ে যায় সাদাপোশাকের এক দল ব্যক্তি। তাঁরা নিজেদের আইনশৃঙ্খলা রক্ষাকারী বাহিনীর সদস্য পরিচয় দিয়েছেন বলে সেখানে উপস্থিত এক সমন্বয়কের স্বজন ও হাসপাতালের চিকিৎসকরা জানান। <span class="date-highlight">২৭ জুলাই</span> বৈষম্যবিরোধী ছাত্র আন্দোলন সংগঠনের আরও দুই সমন্বয়ককে হেফাজতে নেয় গোয়েন্দা শাখা। তারা হলেন সারজিস আলম ও হাসনাত আব্দুল্লাহ। পরে এক অনলাইন সংবাদ সম্মেলনে <span class="date-highlight">২৮ তারিখের (রোববারের)</span> মধ্যে সমন্বয়ক নাহিদ ইসলামসহ, আসিফ মাহমুদসহ আটক সকল শিক্ষার্থীদের মুক্তি, মামলা প্রত্যাহার ও শিক্ষার্থী গণহত্যার সাথে জড়িত মন্ত্রী পর্যায় থেকে কনস্টেবল পর্যন্ত সকল দোষীদের বিরুদ্ধে ব্যবস্থা নিতে আল্টিমেটাম দেন আন্দোলনকারী শিক্ষার্থীরা। একই সাথে রোববার সারা দেশের দেয়ালগুলোতে গ্রাফিতি ও দেয়াল লিখনসহ বিভিন্ন কর্মসূচির ঘোষণা দেয়া হয়।</p>
                  <p><span class="date-highlight">২৮ জুলাই</span> রাত ১০টার দিকে পুলিশি হেফাজতে থাকা ৬ সমন্বয়ক আন্দোলন প্রত্যাহারের ঘোষণা দেন। বিবৃতিতে স্বাক্ষর করেন কোটা সংস্কার আন্দোলনের সমন্বয়ক মো. নাহিদ ইসলাম, মো. সারজিস আলম, হাসনাত আব্দুল্লাহ, মো. আবু বাকের মজুমদার, আসিফ মাহমুদ ও নুসরাত তাবাসসুম। <span class="date-highlight">রবিবার (২৮শে জুলাই)</span> রাতে ডিবি হেফাজতে থেকেই এ সংক্রান্ত এক ভিডিওতে লিখিত বার্তা পাঠান মো. নাহিদ ইসলাম। বিবৃতিতে যা লেখা ছিল তা হুবহু নিচে তুলে ধরা হলো:</p>
                  <blockquote>
                    <p>কোটা সংস্কার আন্দোলন ও তার প্রেক্ষিতে উদ্ভুত পরিস্থিতিতে অনেকেই অপ্রত্যাশিতভাবে আহত এবং নিহত হয়েছেন। তাছাড়া রাষ্ট্রীয় স্থাপনায় অগ্নিসংযোগসহ নানা সহিংস ঘটনা ঘটেছে। আমরা এ সকল অনাকাঙ্খিত ঘটনার তীব্র নিন্দা জানাই এবং সুষ্ঠু তদন্ত সাপেক্ষে দ্রুত বিচারের দাবি জানাচ্ছি।
                      আমাদের প্রধান দাবি ছিল কোটার যৌক্তিক সংস্কার, যা ইতিমধ্যে সরকার পূরণ করেছেন। এখন শিক্ষা প্রতিষ্ঠান খুলে দেওয়ার জন্য সরকারের প্রতি জোর আহবান জানাই। সার্বিক স্বার্থে আমরা এই মুহুর্ত থেকে আমাদের কর্মসূচি প্রত্যাহার করছি।</p>
                  </blockquote>
                  <p>কিন্তু পুলিশি হেফাজতে থাকা অবস্থায় পুলিশের অফিসে বসেই বাকি সমন্বয়কারীদের সাথে যোগাযোগ না করে এমন ঘোষণা দেয়ায় এই ঘোষণাকে সরকার ও পুলিশের চাপে দেয়া হয়েছে বলে আখ্যায়িত করে আন্দোলন চালিয়ে যাওয়ার ঘোষণা দেন বাকিরা। তাদের ফেইসবুক বিবৃতিতে যা লেখা ছিল তা হুবহু নিচে তুলে ধরা হলো:</p>
                  <blockquote>
                    <p>“সিনিয়ররা আগেই নির্দেশনা দিয়েছিলেন এবং বৈষম্যবিরোধী ছাত্র আন্দোলনের শুরু থেকেই নীতিগত সিদ্ধান্ত ছিলো, সমন্বয়কদের মাঝে এক গ্রুপকে তুলে নিয়ে গেলে বাকিরা নেতৃত্ব দেবে। কাউকে যদি আটক করে জোরপূর্বক কোনো বিবৃতি আদায় করা হয়, তবে আমরা সেটা না মেনে যারা মাঠে থাকবো তারা আন্দোলন চালিয়ে যাবো। গণস্বাস্থ্য থেকে তুলে নিয়ে যাওয়ার আগে নাহিদ ভাই, আসিফ ভাই এবং বাকের ভাই তিনজনই স্পষ্ট করে বলেছিলেন, 'আমরা যদি না-ও থাকি আন্দোলন চালিয়ে নিবা, আন্দোলন যাতে থেমে না থাকে। আমাদের উপর অনেক ঝড়-ঝাপটা যাবে, যারা বাইরে থাকবে তারাই অন-কমান্ডে থাকবে। তাই সিনিয়রদের শেষ কথাগুলোকে বুকে ধারণ করেই আমরা আন্দোলন এগিয়ে নিয়ে যাচ্ছি। জোরপূর্বক আদায় করা বিবৃতি বাংলাদেশের ছাত্রসমাজ প্রত্যাখ্যান করছে।”</p>
                  </blockquote>
                  <p><span class="date-highlight">৩১ জুলাই</span> হত্যা, গণগ্রেপ্তার, হামলা, মামলা ও গুমের প্রতিবাদে <span class="date-highlight">৩১ জুলাই</span> বুধবার সারাদেশে বৈষম্যবিরোধী ছাত্র আন্দোলন <span class="date-highlight">‘মার্চ ফর জাস্টিস’</span> (ন্যায়বিচারের জন্য পদযাত্রা) কর্মসূচি পালন করে।</p> 
                  <p><span class="date-highlight">১ আগস্ট</span> গোয়েন্দা পুলিশের (ডিবি) হেফাজতে থাকা বৈষম্যবিরোধী ছাত্র আন্দোলনের ছয় সমন্বয়ককে বেলা দেড়টার একটু পরেই ছেড়ে দেওয়া হয়। বৈষম্যবিরোধী ছাত্র আন্দোলন <span class="date-highlight">২ আগস্ট শুক্রবার</span> <span class="date-highlight">‘প্রার্থনা ও ছাত্র-জনতার গণমিছিল’</span> কর্মসূচি ঘোষণা করে। <span class="date-highlight">২ আগস্ট</span> গোয়েন্দা পুলিশের (ডিবি) হেফাজত থেকে ছাড়া পাওয়া বৈষম্যবিরোধী আন্দোলনের ছয় সমন্বয়ক গণমাধ্যমে পাঠানো এক বিবৃতিতে বলেন যে আন্দোলন প্রত্যাহার করে ডিবি অফিস থেকে প্রচারিত ছয় সমন্বয়কের ভিডিও বিবৃতিটি তারা স্বেচ্ছায় দেননি।</p>
                </div>

